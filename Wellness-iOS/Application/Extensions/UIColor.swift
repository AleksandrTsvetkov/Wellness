import UIKit

extension UIColor {
    
    class var customLightGray: UIColor {
        return UIColor.init(red: 241/255, green: 241/255, blue: 241/255, alpha: 1)
    }
    
    class var customRed: UIColor {
        return UIColor.init(red: 255/255, green: 103/255, blue: 98/255, alpha: 1)
    }
    
    class var customGray: UIColor {
        return UIColor.init(red: 197/255, green: 197/255, blue: 197/255, alpha: 1)
    }
    
    class var customDarkGray: UIColor {
        return UIColor.init(red: 99/255, green: 99/255, blue: 99/255, alpha: 1)
    }
    
    class var customDarkRed: UIColor {
        return UIColor.init(red: 213/255, green: 85/255, blue: 99/255, alpha: 1)
    }
    
    class var selectedCellColor: UIColor {
        return UIColor.init(red: 254/255, green: 240/255, blue: 239/255, alpha: 1)
    }
    
    class var deselectedCellColor: UIColor {
        return UIColor.init(red: 249/255, green: 249/255, blue: 249/255, alpha: 1)
    }
    
    class var main: UIColor {
        return UIColor.init(red: 250/255, green: 114/255, blue: 104/255, alpha: 1)
    }
    
    class var second: UIColor {
        return UIColor.init(red: 195/255, green: 66/255, blue: 63/255, alpha: 1)
    }
}
